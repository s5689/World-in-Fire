class Anim_2_confeti {
  async start(e) {
    const sprite = [];
    const confeti = ['"', ".", "°", "`", "^", "'", ","];
    let ySize = 1;
    let yStart;
    let y = 0;

    // Configurar posicion de confeti segun el argumento
    if (e === 0) yStart = -1;
    else yStart = 11;

    while (y <= 30) {
      for (let k = ySize; k >= 0; k--) {
        let txt = "";
        let col = "";

        // Armar confeti
        for (let j = 0; j < 4; j++) {
          let temp = getColorNumber(Math.round(Math.random() * 5) + 9);
          txt += confeti[Math.round(Math.random() * 6)];

          if (temp === 9) col += 9;
          if (temp === 10) col += A;
          if (temp === 11) col += B;
          if (temp === 12) col += C;
          if (temp === 13) col += D;
          if (temp === 14) col += E;

          col += " ";
          txt += " ";
        }

        // Limpiar confeti en posiciones anteriores
        clean({ x: 3, y: yStart + k - 1, o: { text: [txt] } });
        clean({ x: 70, y: yStart + k - 1, o: { text: [txt] } });

        // Dibujar confeti en ambos lados
        if (e === 0) {
          if (yStart + k <= 8) {
            draw({ x: 3, y: yStart + k, o: { text: [txt], color: [col] } });
            draw({ x: 70, y: yStart + k, o: { text: [txt], color: [col] } });
          }
        } else {
          if (yStart + k <= 19) {
            draw({ x: 3, y: yStart + k, o: { text: [txt], color: [col] } });
            draw({ x: 70, y: yStart + k, o: { text: [txt], color: [col] } });
          }
        }
      }

      y++;
      if (ySize <= 11) ySize++;
      else {
        ySize--;
        yStart++;
      }

      await sleep(25);
    }
  }
}

